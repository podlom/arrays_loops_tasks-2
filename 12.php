<?php

// цикл while
$n = 1000;
$num = 0;
while ($n>50){
    $n /= 2;
    $num++;
}
echo "Получилось число - ".$n."<br>";
echo "Количество итераций - ".$num;

echo "<hr>";

// цикл for
$num = 0;
for($n=1000; $n>50; $n/=2){
    $num++;
}
echo "Получилось число - ".$n."<br>";
echo "Количество итераций - ".$num;